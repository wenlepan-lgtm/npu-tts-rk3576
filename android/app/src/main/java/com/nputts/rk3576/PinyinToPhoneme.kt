 * 中文文本 → MeloTTS phoneme/tone/language ID 数组
 *
 * 数据来源：
 * - pypinyin (汉字→拼音，覆盖 20920 个 CJK 字符)
 * - opencpop-strict.txt (拼音→音素符号，MeloTTS 官方映射)
 * - tokens.txt (音素符号→ID)
 *
 * 流程: 中文文本 → 逐字查拼音 → 拼音查音素ID → 插入 blank token
