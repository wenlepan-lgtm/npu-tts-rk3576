object PinyinToPhoneme {
    private const val TAG = "PinyinToPhoneme"
    private const val ID_BLANK = 0
    private const val ID_UNK = 111
    private const val ID_SP = 110
    private const val LANG_ZH = 3  // MeloTTS: 3=ZH (NOT 0)

    data class PhonemeResult(
        val phones: IntArray,
        val tones: IntArray,
        val langs: IntArray
    )

    // 标点 → phoneme ID
    private val punctMap = mapOf(
        '！' to 103, '!' to 103,
        '？' to 104, '?' to 104,
        '，' to 106, ',' to 106,
        '。' to 107, '.' to 107,
        '、' to 106,
        '；' to 106, ';' to 106,
        '：' to 106, ':' to 106,
        '…' to 105
    )

    // 汉字→音素ID数组+声调 (由 pypinyin + opencpop-strict.txt 生成)
    // IntArray = [phoneme_id1, phoneme_id2?, tone]
    // 例: "中" → intArrayOf(101, 67, 1) = zh(101) + ong(67) + tone1
    private val charMap: Map<Char, IntArray> by lazy { buildCharPinyinMap() }

    /**
     * 将中文文本转为 MeloTTS 音素序列
     * 使用 intersperse 模式: [0, p1, 0, p2, 0, p3, 0]
     * 每个音素前后都插入 blank=0，与 Python 参考一致
     */
    fun convert(text: String): PhonemeResult {
        // Step 1: 收集所有原始音素
        val rawPhones = mutableListOf<Int>()
        val rawTones = mutableListOf<Int>()

        for (ch in text) {
            when {
                ch.isWhitespace() -> {
                    rawPhones.add(ID_SP); rawTones.add(0)
                }
                punctMap.containsKey(ch) -> {
                    rawPhones.add(punctMap[ch]!!); rawTones.add(0)
                }
                ch.code in 0x4E00..0x9FFF -> {
                    val entry = charMap[ch]
                    if (entry != null) {
                        val tone = entry.last()
                        for (i in 0 until entry.size - 1) {
                            rawPhones.add(entry[i]); rawTones.add(tone)
                        }
                    } else {
                        Log.w(TAG, "OOV char: $ch, using UNK")
                        rawPhones.add(ID_UNK); rawTones.add(0)
                    }
                }
                else -> {
                    rawPhones.add(ID_UNK); rawTones.add(0)
                }
            }
        }

        // Step 2: intersperse — 在每个音素前插入 blank: [0, p1, 0, p2, 0, ..., 0]
        val phones = mutableListOf<Int>()
        val tones = mutableListOf<Int>()
        val langs = mutableListOf<Int>()

        for (i in rawPhones.indices) {
            phones.add(ID_BLANK); tones.add(0); langs.add(LANG_ZH)
            phones.add(rawPhones[i]); tones.add(rawTones[i]); langs.add(LANG_ZH)
        }
        phones.add(ID_BLANK); tones.add(0); langs.add(LANG_ZH)  // trailing blank

        return PhonemeResult(
            phones = phones.toIntArray(),
            tones = tones.toIntArray(),
            langs = langs.toIntArray()
        )
    }

